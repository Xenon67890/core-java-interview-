package com.rays.basics;

public class AutoboxingUnboxing {
	
	public static void main(String[] args) {
		
		// Autoboxing
		
		int a = 10;
		
		Integer i = a;
		
		System.out.println("Autoboxing = " + i);
		
		// Unboxing
		
		Integer x = 50;
		
		int y = x;
		
		System.out.println("Unboxing = " + y);
	}

}
